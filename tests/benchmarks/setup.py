"""One-time setup: signup, create project, create API key.

Run this once before the benchmarks::

    python -m tests.benchmarks.setup

This creates a benchmark org + dashboard user (if not exists), a dedicated
project, and a project-scoped API key.  Writes ``tests/benchmarks/.env``
with the project ID and API key so that runner scripts can find them.
"""

from __future__ import annotations

import asyncio
import os
import sys
from pathlib import Path

import httpx

# Add the project root so we can import core.config if needed
sys.path.insert(0, str(Path(__file__).resolve().parents[2]))

BENCH_DIR = Path(__file__).resolve().parent
ENV_FILE = BENCH_DIR / ".env"

# ── Config from environment ───────────────────────────────────────────────────

BASE_URL: str = os.getenv("OPENZEP_BASE_URL", "http://localhost:8000")
"""OpenZep API base URL."""

SIGNUP_EMAIL: str = os.getenv("BENCH_SIGNUP_EMAIL", "benchmark@openzep.ai")
SIGNUP_PASSWORD: str = os.getenv("BENCH_SIGNUP_PASSWORD", "bench-p@ssword-2026")
SIGNUP_ORG: str = os.getenv("BENCH_SIGNUP_ORG", "OpenZep Benchmarks")
PROJECT_NAME: str = os.getenv("BENCH_PROJECT_NAME", "DMR+LongMemEval Benchmark")
API_KEY_NAME: str = os.getenv("BENCH_API_KEY_NAME", "benchmark-key")


# ── HTTP helpers ───────────────────────────────────────────────────────────────


async def _signup() -> str:
    """Sign up and return a JWT access token."""
    url = f"{BASE_URL}/v1/auth/signup"
    async with httpx.AsyncClient() as client:
        resp = await client.post(
            url,
            json={
                "email": SIGNUP_EMAIL,
                "password": SIGNUP_PASSWORD,
                "organization_name": SIGNUP_ORG,
            },
        )
        if resp.status_code == 409:
            # Already exists — log in instead
            return await _login()
        resp.raise_for_status()
        data = resp.json()
        print(f"[setup] Signed up as {SIGNUP_EMAIL} (org: {SIGNUP_ORG})")
        return data["access_token"]


async def _login() -> str:
    """Log in and return a JWT access token."""
    url = f"{BASE_URL}/v1/auth/login"
    async with httpx.AsyncClient() as client:
        resp = await client.post(
            url,
            json={"email": SIGNUP_EMAIL, "password": SIGNUP_PASSWORD},
        )
        resp.raise_for_status()
        data = resp.json()
        print(f"[setup] Logged in as {SIGNUP_EMAIL}")
        return data["access_token"]


async def _create_project(token: str) -> dict:
    """Create the benchmark project. Returns the project dict."""
    url = f"{BASE_URL}/v1/projects"
    async with httpx.AsyncClient() as client:
        resp = await client.post(
            url,
            json={"name": PROJECT_NAME, "description": "Benchmark: DMR + LongMemEval"},
            headers={"Authorization": f"Bearer {token}"},
        )
        if resp.status_code == 409:
            # Already exists — list and find it
            list_resp = await client.get(
                url,
                headers={"Authorization": f"Bearer {token}"},
            )
            list_resp.raise_for_status()
            projects = list_resp.json()
            for p in projects:
                if p["name"] == PROJECT_NAME:
                    print(f"[setup] Found existing project: {p['id']} ({p['name']})")
                    return p
            raise RuntimeError(f"Project '{PROJECT_NAME}' not found after 409")
        resp.raise_for_status()
        project = resp.json()
        print(f"[setup] Created project: {project['id']} ({project['name']})")
        return project


async def _create_api_key(token: str, project_id: str) -> str:
    """Create a project-scoped API key. Returns the raw key string."""
    url = f"{BASE_URL}/v1/projects/{project_id}/api-keys"
    async with httpx.AsyncClient() as client:
        resp = await client.post(
            url,
            json={"name": API_KEY_NAME},
            headers={"Authorization": f"Bearer {token}"},
        )
        resp.raise_for_status()
        data = resp.json()
        print(f"[setup] Created API key: {data['prefix']}... (id: {data['id']})")
        return data["raw_key"]


def _write_env(project_id: str, api_key: str) -> None:
    """Write a ``.env`` file so runners can find the project + key."""
    content = (
        f"# Generated by tests/benchmarks/setup.py\n"
        f"BENCH_API_KEY={api_key}\n"
        f"BENCH_PROJECT_ID={project_id}\n"
        f"OPENZEP_BASE_URL={BASE_URL}\n"
    )
    ENV_FILE.write_text(content)
    print(f"[setup] Wrote {ENV_FILE}")


async def main() -> None:
    """Full setup flow."""
    print("=" * 60)
    print("OpenZep Benchmark Setup")
    print("=" * 60)

    token = await _signup()
    project = await _create_project(token)
    raw_key = await _create_api_key(token, project["id"])
    _write_env(project["id"], raw_key)

    print("\n✅  Setup complete.")
    print(f"   Project ID: {project['id']}")
    print(f"   API Key:    {raw_key[:12]}...")
    print(f"   .env file:  {ENV_FILE}")


if __name__ == "__main__":
    asyncio.run(main())
